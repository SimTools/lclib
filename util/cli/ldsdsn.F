      SUBROUTINE LDSDSN( DSNAME )
C
C ARG
      CHARACTER * (*)  DSNAME
C
C VAR
#if defined(G77_521_OR_GREATER)
      INTEGER   *  4  USERID
#else
      CHARACTER *  4  USERID
#endif
      CHARACTER * 72  RECORD
      CHARACTER * 80  CMD
      CHARACTER * 100  ALOCFB, FREEFB
C
C BEGIN
      ALOCFB = 'ALLOC DD(FT06F001) DS(@.PIPE) REUSE SHR'
      FREEFB = 'FREE DD(FT06F001)'
#if defined(G77_521_OR_GREATER)
      USERID = GETUID()
#else
      CALL GETUID( USERID )
#endif
      CMD(1:4) = 'LDS '
      LCMD = 4
      LDSN = LENRD( DSNAME )
      CMD(LCMD+1:LCMD+LDSN) = DSNAME(1:LDSN)
      LCMD = LCMD + LDSN
      CALL IPFCMD( IRET1,IRET2,ALOCFB,LENRD(ALOCFB) )
      CALL IPFCMD( IRET1,IRET2,CMD,LCMD )
      CALL IPFCMD( IRET1,IRET2,FREEFB,LENRD(FREEFB) )
      RETURN
      END
